Critter::Critter(int hambre, int aburrimiento) 
{
	m_hambre = hambre;
	m_aburrimiento = aburrimiento;
}

void Critter::comer(int comida) 
{
	m_hambre -= comida;
	cout << "MMMMH QUE RICOOOO gracias a esto ahora tengo " << m_hambre << " de hambre :)\n";

}

void Critter::jugar(int diversion) 
{
	m_aburrimiento -= diversion;
	cout << "AJJAJAJAJAJ QUE BIEN ME LO PASO gracias a este juego ahora tengo " << m_aburrimiento << " de aburrimiento :)\n";
}

int Critter::GetMood() const
{
	int mood = m_hambre + m_aburrimiento;
	return mood;
}

void Critter::hablar() 
{
	cout << "Ahora misom tengo " << m_hambre << " de hambre y " << m_aburrimiento << " de aburrimiento.\n";

	if (GetMood() >= 15)
	{
		caraEnfadada();
		cout << "Estoy mui mal loco, necesitaria dormir un poco :v\n";
	}

	else if (GetMood() >= 10)
	{
		caraDefault();
		cout << "Esoty regulin regular la verdad :(\n";
	}

	else
	{
		caraContenta();
		cout << "De momento estoy bien lokete :)\n";
	}
}

void Critter::PasaTiempo(int tiempo )
{
	m_hambre += tiempo;
	m_aburrimiento += tiempo;

	if (m_hambre < 0) m_hambre = 0;
	if (m_aburrimiento < 0) m_aburrimiento = 0;
}

void Critter::dormir(int comida, int diversion)
{
	caraDormido();
	m_hambre += comida;
	m_aburrimiento += diversion;
	cout << "Que bien me ha ido esta siesta, la necesitaba! PERO AHORA ESTOY SUPER HAMBRIENTO Y ABURRIDO\n";
}
